def extract_scalars(kps, fps=None):
    if fps is None:
        fps = FPS   # default 30; override with actual video fps when available
    kps = safe_np(kps, clip=5.0)

    def _ang(a, b, c):
        ba, bc = a - b, c - b
        d = np.maximum(
            np.linalg.norm(ba, axis=1) * np.linalg.norm(bc, axis=1), EPS
        )
        return np.degrees(np.arccos(np.clip(np.sum(ba*bc, axis=1) / d, -1, 1)))

    def _sg(arr):
        w = min(11, len(arr) - 1 if len(arr) % 2 == 0 else len(arr))
        w = max(w if w % 2 == 1 else w - 1, 5)
        try:    return savgol_filter(arr, w, 2)
        except: return arr

    lk   = _ang(kps[:,0,:], kps[:,1,:], kps[:,2,:])
    rk   = _ang(kps[:,3,:], kps[:,4,:], kps[:,5,:])
    lk_s = _sg(lk)
    rk_s = _sg(rk)

    lr  = float(np.nanmax(lk) - np.nanmin(lk))
    rr  = float(np.nanmax(rk) - np.nanmin(rk))
    sym = abs(lr-rr) / ((lr+rr)/2 + EPS) * 100 if (lr+rr) > 1 else 0.0

    # Velocity: 95th-percentile of |frame-to-frame angle change| × fps
    # Using max() picks up single jitter spikes and fires on normal squats.
    # 95th-percentile is robust to 1–2 noisy frames while still catching
    # genuinely fast movement.
    if len(lk_s) > 1:
        diffs = np.abs(np.diff(lk_s))
        vel   = float(np.percentile(diffs, 95) * fps)
    else:
        vel = 0.0

    # Jerk from angle signal (not raw coords — too small after normalisation)
    acc  = np.gradient(np.gradient(lk_s))
    jerk = float(np.nanmean(np.abs(np.gradient(acc))))

    hip   = (kps[:,0,:] + kps[:,3,:]) / 2.0
    pk    = int(np.argmin(lk))
    trunk = float(abs(hip[pk, 0] - hip[0, 0]))

    lkc, rkc = lk_s - np.nanmean(lk_s), rk_s - np.nanmean(rk_s)
    lag = float(abs(
        np.argmax(np.correlate(lkc, rkc, 'full')) - (len(lk_s) - 1)
    )) if np.nanstd(lkc) > EPS and np.nanstd(rkc) > EPS else 0.0

    # Smoothness: ratio of frame-to-frame std to movement range.
    # Near 0 = jerky (std ~ range), near 1 = smooth (std << range).
    # Robust across different exercises and normalisation scales.
    range_proxy = (lr + rr) / 2.0 + EPS
    smooth = float(np.clip(1.0 - np.nanstd(np.diff(lk_s)) / range_proxy, 0, 1))

    return safe_np(np.array([
        np.clip(lr / 180,         0, 1),
        np.clip(rr / 180,         0, 1),
        np.clip(np.min(lk) / 180, 0, 1),
        np.clip(np.min(rk) / 180, 0, 1),
        np.clip(sym / 100,        0, 5),
        np.clip(vel / 200,        0, 5),
        np.clip(jerk / 10,        0, 5),
        np.clip(trunk,            0, 5),
        np.clip(lag / TARGET_LEN, 0, 1),
        smooth,
    ], dtype=np.float32), fill=0.0, clip=10.0)

# Distribution alignment: maps MediaPipe scalars to Vicon training distribution
def align_scalars(sc):
    if not SCALAR_ALIGN:
        return sc.copy()
    mp_std_safe = np.where(MP_STD < 1e-6, 1.0, MP_STD)
    z = (sc - MP_MEAN) / mp_std_safe
    # Guard: clip z to ±3 before mapping. Without this, a single bad constant
    # (e.g. MP_STD[9]=0.002 when smooth is now 0.98) causes z=487 → clipped
    # to 5.0 → the feature is destroyed for every rep.
    z = np.clip(z, -3.0, 3.0)
    return np.clip(z * TRAIN_STD + TRAIN_MEAN, 0.0, 5.0).astype(np.float32)

