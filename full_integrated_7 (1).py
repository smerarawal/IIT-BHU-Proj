def adapt_exercise_weights(state, old_list, new_list):
    old_n, new_n = len(old_list), len(new_list)
    if old_n == new_n: return state
    print(f'Adapting exercise heads: {old_n} → {new_n}')
    for key_w, key_b in [('ex_emb.weight', None), ('ex_head.weight', 'ex_head.bias')]:
        if key_w in state:
            old_w = state[key_w]
            new_w = torch.randn(new_n, old_w.shape[1]) * 0.02
            for old_i, name in enumerate(old_list):
                if name in new_list: new_w[new_list.index(name)] = old_w[old_i]
            state[key_w] = new_w
        if key_b and key_b in state:
            old_b = state[key_b]
            new_b = torch.zeros(new_n)
            for old_i, name in enumerate(old_list):
                if name in new_list: new_b[new_list.index(name)] = old_b[old_i]
            state[key_b] = new_b
    return state

