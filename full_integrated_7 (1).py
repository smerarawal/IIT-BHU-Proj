def compute_torque(quality, mode):
    m = HARDWARE_MODES.get(mode, HARDWARE_MODES[0])
    return float(np.clip(m['scale']*quality + m['bias'], m['min'], m['max']))

def generate_feedback(label, quality, scalars):
    s = np.asarray(scalars)
    triggered = sorted([r for r in FEEDBACK_RULES if r['check'](s)],
                       key=lambda r: r['priority'], reverse=True)
    flags = [r['name'] for r in triggered]
    if label == 1 and quality >= 0.75:
        return {'message':'Great rep — keep it up!','short':'Great rep',
                'flags':[],'severity':'good'}
    if label == 1 and quality >= 0.5:
        return {'message':'Good rep — maintain that form.','short':'Good rep',
                'flags':flags,'severity':'good'}
    if triggered:
        top = triggered[0]
        return {'message':top['message'],'short':top['short'],
                'flags':flags,'severity':'error' if top['priority']>=8 else 'warning'}
    return {'message':'Focus on smooth, controlled movement.',
            'short':'Keep it smooth','flags':[],'severity':'warning'}

def build_hardware_payload(sample, quality, scalars, torque, mode):
    kps = sample['keypoints']
    def _ang(a, b, c):
        ba, bc = a-b, c-b
        d = np.linalg.norm(ba, axis=1)*np.linalg.norm(bc, axis=1) + EPS
        return np.degrees(np.arccos(np.clip(np.sum(ba*bc, axis=1)/d, -1, 1)))
    lk = _ang(kps[:,0,:], kps[:,1,:], kps[:,2,:])
    rk = _ang(kps[:,3,:], kps[:,4,:], kps[:,5,:])
    l_peak = float(np.min(lk)); r_peak = float(np.min(rk))
    l_rom  = float(np.max(lk)-l_peak); r_rom = float(np.max(rk)-r_peak)
    ex     = sample.get('exercise','unknown')
    target = TARGET_ANGLES.get(ex, TARGET_ANGLES['default'])
    l_def  = max(0., target-l_peak); r_def = max(0., target-r_peak)
    return {
        'torque': round(torque,4), 'mode': HARDWARE_MODES[mode]['name'],
        'mode_int': mode,
        'joint': {'left_knee_peak_deg': round(l_peak,2),
                  'right_knee_peak_deg': round(r_peak,2),
                  'left_knee_rom_deg': round(l_rom,2),
                  'right_knee_rom_deg': round(r_rom,2)},
        'target': {'knee_target_deg': target,
                   'left_deficit_deg': round(l_def,2),
                   'right_deficit_deg': round(r_def,2)},
        'torque_per_degree': round(torque/max(l_def,1.0), 5),
    }

@torch.no_grad()
def infer_sample(model, sample, device=DEVICE, hardware_mode=0, debug=False):
    model.eval()
    kps_t  = torch.from_numpy(sample['keypoints']).permute(2,0,1).unsqueeze(0).to(device)
    adj_t  = torch.from_numpy(ADJ).to(device)
    # FIX: apply distribution alignment before feeding to model
    aligned_sc = align_scalars(sample['scalars'])
    sc_t   = torch.from_numpy(aligned_sc).unsqueeze(0).to(device)
    mode_t = torch.tensor([hardware_mode], dtype=torch.long, device=device)
    ex_t   = torch.tensor([sample['exercise_idx']], dtype=torch.long, device=device)

    if debug:
        sc_raw = sample['scalars']
        names  = ['l_rom','r_rom','l_peak','r_peak','sym','vel','jerk','trunk','lag','smooth']
        scales = [180,180,180,180,100,200,10,1,150,1]
        mp_std_safe = np.where(MP_STD < 1e-6, 1.0, MP_STD)
        z_scores = (sc_raw - MP_MEAN) / mp_std_safe if SCALAR_ALIGN else np.zeros(10)
        print(f'    Scalar debug [{sample.get("rep_id","?")}]  '
              f'(align={"ON" if SCALAR_ALIGN else "OFF"}):')
        for i, n in enumerate(names):
            z_warn = f'  ⚠ z={z_scores[i]:.1f}!' if abs(z_scores[i]) > 3 else ''
            print(f'      {n:10s}  raw={sc_raw[i]:.4f}  '
                  f'phys={sc_raw[i]*scales[i]:.2f}  '
                  f'aligned={aligned_sc[i]:.4f}{z_warn}')

    logits, quality, *_ = model(kps_t, adj_t, sc_t, mode_t, ex_t)
    label   = int(logits.argmax(1).item())
    quality = float(torch.sigmoid(quality).item())

    # NOTE: no rule-based override — trust the model.
    # An override that fires on quality < 0.4 (most reps) and uses a
    # trivially-low ROM threshold (>25°) will force label=1 on almost
    # every rep, producing the 5/5 CORRECT artefact seen in testing.
    # If classification is still wrong after scalar alignment is verified,
    # the fix is fine-tuning on MediaPipe data, not a hardcoded rule.

    sc_raw = sample['scalars']
    torque = compute_torque(quality, hardware_mode)
    fb     = generate_feedback(label, quality, sc_raw)
    hw     = build_hardware_payload(sample, quality, sc_raw, torque, hardware_mode)

    return {
        'rep_id':         sample.get('rep_id','?'),
        'exercise':       sample.get('exercise','unknown'),
        'label':          label,
        'correct':        label == 1,
        'quality_score':  round(quality, 4),
        'feedback':       fb['message'],
        'feedback_short': fb['short'],
        'severity':       fb['severity'],
        'flags':          fb['flags'],
        'hardware':       hw,
        'torque_signal':  torque,
        'mode_name':      HARDWARE_MODES[hardware_mode]['name'],
        'scalars':        sc_raw.tolist(),
        'scalars_aligned': aligned_sc.tolist(),
    }

def _predict_exercise_from_samples(model, samples, device=DEVICE):
    if not samples: return 'unknown', EXERCISE2IDX['unknown']
    votes, adj_t = [], torch.from_numpy(ADJ).to(device)
    model.eval()
    with torch.no_grad():
        for s in samples:
            kps_t = torch.from_numpy(s['keypoints']).permute(2,0,1).unsqueeze(0).to(device)
            pred_ex, pred_idx = model.predict_exercise(kps_t, adj_t)
            votes.append(pred_ex)
    counts  = Counter(votes)
    pred_ex = counts.most_common(1)[0][0]
    print(f'[Exercise Classification] Votes: {dict(counts)}')
    return pred_ex, EXERCISE2IDX.get(pred_ex, EXERCISE2IDX['unknown'])

