def ps1_root_center(df):
    for ax in ['x','y','z']:
        mid = (df[f'HipLeft_{ax}'] + df[f'HipRight_{ax}']) / 2.0
        for jn in ['HipLeft','KneeLeft','AnkleLeft',
                   'HipRight','KneeRight','AnkleRight']:
            df[f'{jn}_{ax}'] = df[f'{jn}_{ax}'] - mid
    return df

